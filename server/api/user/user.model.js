'use strict';

var mongoose = require('mongoose');
var Schema = mongoose.Schema;
var crypto = require('crypto');
var authTypes = ['github'];
var github = require('octonode');

var UserSchema = new Schema({
  name: String,
  email: {
    type: String,
    lowercase: true
  },
  role: {
    type: String,
    default: 'user'
  },
  hashedPassword: String,
  provider: String,
  salt: String,
  github: {},
  linkedin: {},
  skills: [],
  languages: {}
});

// Virtuals
UserSchema
  .virtual('password')
  .set(function(password) {
    this._password = password;
    this.salt = this.makeSalt();
    this.hashedPassword = this.encryptPassword(password);
  })
  .get(function() {
    return this._password;
  });

// Public profile information
UserSchema
  .virtual('profile')
  .get(function() {
    return {
      'name': this.name,
      'role': this.role
    };
  });

// Non-sensitive info we'll be putting in the token
UserSchema
  .virtual('token')
  .get(function() {
    return {
      '_id': this._id,
      'role': this.role
    };
  });

// Validations

// Validate empty email
UserSchema
  .path('email')
  .validate(function(email) {
    if (authTypes.indexOf(this.provider) !== -1) return true;
    return email.length;
  }, 'Email cannot be blank');

// Validate empty password
UserSchema
  .path('hashedPassword')
  .validate(function(hashedPassword) {
    if (authTypes.indexOf(this.provider) !== -1) return true;
    return hashedPassword.length;
  }, 'Password cannot be blank');

// Validate email is not taken
UserSchema
  .path('email')
  .validate(function(value, respond) {
    var self = this;
    this.constructor.findOne({
      email: value
    }, function(err, user) {
      if (err) throw err;
      if (user) {
        if (self.id === user.id) return respond(true);
        return respond(false);
      }
      respond(true);
    });
  }, 'The specified email address is already in use.');

var validatePresenceOf = function(value) {
  return value && value.length;
};

// Pre-save hook
UserSchema
  .pre('save', function(next) {
    if (!this.isNew) return next();

    if (!validatePresenceOf(this.hashedPassword) && authTypes.indexOf(this.provider) === -1)
      next(new Error('Invalid password'));
    else
      next();
  });

// Methods
UserSchema.methods = {
  // Authenticate - check if the passwords are the same/
  // Returns a {Boolean}
  authenticate: function(plainText) {
    return this.encryptPassword(plainText) === this.hashedPassword;
  },

  getSkills: function(token) {
    var self = this;
    var client = github.client(token);

    client.get('/users/' + self.github.login + '/repos', {}, function(err, status, repos, headers) {
      var totalBytes = 0;
      var languages = {};
      var count = 0;
      for (var i = 0; i < repos.length; i++) {
        var url = '/repos/' + self.github.login + '/' + repos[i].full_name.split('/')[1] + '/languages';
        client.get(url, function(err, status, body, headers) {
          for (var key in body) {
            totalBytes += body[key];
            if (languages.hasOwnProperty(key)) {
              languages[key] += body[key];
            } else {
              languages[key] = body[key];
            }
          }
          count++;
          if (count === repos.length) {
            // done, save to db
            self.languages = languages;
            self.save(function(err) {
              if (err) throw err;
            });
          }
        });
      }
    });
  },

  // Make salt
  makeSalt: function() {
    return crypto.randomBytes(16).toString('base64');
  },

  // Encrypt password
  encryptPassword: function(password) {
    if (!password || !this.salt) return '';
    var salt = new Buffer(this.salt, 'base64');
    return crypto.pbkdf2Sync(password, salt, 10000, 64).toString('base64');
  }
};

module.exports = mongoose.model('User', UserSchema);